package com.saniazt.test;

public class codeWars1 {

  public int countSheeps(Boolean[] arrayOfSheeps) {
    int z = 0;
    for (int i = 0; i < arrayOfSheeps.length; i++) {
      if (arrayOfSheeps[i] = true) {
        z++;
      }
    }
    return z;
  }

  public static void main(String[] args) {}
}
